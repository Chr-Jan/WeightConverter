        public static string SwitchItUp(int number)
        {
            switch (number)
            {
                case 1:
                    Console.WriteLine("One");
                    break;
                case 3:
                    Console.WriteLine("Three");
                    break;
                case 5:
                    Console.WriteLine("Five");
                    break;
            }
        }
